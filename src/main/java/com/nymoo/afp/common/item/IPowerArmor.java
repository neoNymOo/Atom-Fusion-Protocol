package com.nymoo.afp.common.item;

/**
 * Интерфейс-маркер для идентификации предметов силовой брони.
 * Определяет базовую функциональность для всех типов силовой брони в моде.
 */
public interface IPowerArmor {

    /**
     * Возвращает тип силовой брони.
     * Используется для идентификации различных наборов брони и их свойств.
     *
     * @return Строковый идентификатор типа брони (например, "x-01", "t-60", "exo")
     */
    String getPowerArmorType();
}